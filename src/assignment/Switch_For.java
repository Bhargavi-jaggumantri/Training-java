package assignment;

public class Switch_For {
	public static void main(String[] args) {
		for(int num=1; num<=5;num++) {
			switch(num) {
			case 1:
				System.out.println("one");
				break;
			case 2:
				System.out.println("two");
				break;
			
		    case 3:
			    System.out.println("three");
		        break;
		    case 4:
		    	System.out.println("four");
		    	break;
		    default:
		    	System.out.println("five");
			}
			
			
		}
	}

}
